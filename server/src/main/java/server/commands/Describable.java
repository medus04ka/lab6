package server.commands;

/**
 * Что-то, что можно назвать и описать.
 *
 */
public interface Describable {
    /**
     * Получить имя.
     *
     * @return имя name
     */
    String getName();

    /**
     * Получить описание.
     *
     * @return описание description
     */
    String getDescription();
}